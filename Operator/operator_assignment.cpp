#include<iostream>
using namespace std;
int main()
{
    int a = 3, b = 2;
    a += b;                                // a += b ,      a = a + b
    cout<<"a = "<<a<<endl;                // summation assigned on a ,      Now a = 5

    a -= b;                              // a -= b ,     a = a - b
    cout<<"a = "<<a<<endl;              // subtraction assigned on a ,     Now a = 3
}
